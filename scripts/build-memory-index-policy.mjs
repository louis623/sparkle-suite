// Regenerate app/api/compile-memory-index/policy.ts from the source markdown
// Editorial Policy. Computes SHA-256 of the raw text and writes a TypeScript
// module exporting the policy string + version + hash.
//
// Run with:  node scripts/build-memory-index-policy.mjs
//
// Source: pass the markdown path as argv[2], or default to the v1.2 file in
// ~/Downloads (with a "(1)" suffix from a duplicate download).

import { readFileSync, writeFileSync } from 'node:fs'
import { createHash } from 'node:crypto'
import { resolve } from 'node:path'
import { homedir } from 'node:os'

const DEFAULT_SOURCE = resolve(
  homedir(),
  'Downloads',
  'NR_Memory_Index_Editorial_Policy_v1_2 (1).md',
)
const OUTPUT = resolve(
  process.cwd(),
  'app',
  'api',
  'compile-memory-index',
  'policy.ts',
)
const VERSION = '1.2'

const sourcePath = process.argv[2] ?? DEFAULT_SOURCE
const text = readFileSync(sourcePath, 'utf8')

const hash = createHash('sha256').update(text, 'utf8').digest('hex')

const banner = `// AUTO-GENERATED by scripts/build-memory-index-policy.mjs — DO NOT EDIT BY HAND.
// Source: ${sourcePath.replace(/\\/g, '/').split('/').pop()} (version ${VERSION})
// Regenerate with:
//   node scripts/build-memory-index-policy.mjs [path-to-source.md]
//
// The Editorial Policy ships as a compiled-in string constant because the
// Vercel route cannot read arbitrary files at runtime. The SHA-256 is stored
// on every compile audit row so we can prove which policy text produced
// which output.
`

const body = `
export const POLICY_VERSION = ${JSON.stringify(VERSION)};

export const EDITORIAL_POLICY = ${JSON.stringify(text)};

export const POLICY_HASH = ${JSON.stringify(hash)};
`

writeFileSync(OUTPUT, banner + body, 'utf8')

console.log(`Wrote ${OUTPUT}`)
console.log(`Version: ${VERSION}`)
console.log(`SHA-256: ${hash}`)
console.log(`Source bytes: ${Buffer.byteLength(text, 'utf8')}`)
